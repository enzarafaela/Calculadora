    private void insertCharInInputLabel(String inputUser) {
        this.JInputLBL.setText(this.JInputLBL.getText()+inputUser);
    }
    
    private void insertCharInOperationLabel(boolean result) {
        if (result) {
            this.JOperationLBL.setText(this.JOperationLBL.getText()+ " " + this.secondNumber);
            return;
        }
        this.JOperationLBL.setText(this.firstNumber + " " + this.operator);
        this.JInputLBL.setText("");
    }
    
    private void insertResultInLabel() {
        this.JInputLBL.setText(this.result);
    }
    
    private void clear() {
        this.JInputLBL.setText(this.JInputLBL.getText().substring(0,this.JInputLBL.getText().length()-1));
    }
    
