    private void JDotbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JDotbtnActionPerformed
        if (this.result.length() > 0) {
            return;
        }
        if (this.operator == null || this.operator.isEmpty()) {
            if (this.firstNumber.contains(".")) {
                return;
            }
            if (this.firstNumber.length() == 0) {
                this.firstNumber = this.firstNumber+"0.";
                this.insertCharInInputLabel("0.");
                return;
            }
            this.firstNumber = this.firstNumber+".";
            this.insertCharInInputLabel(".");
            return;
        }
        if (this.secondNumber.contains(".")) {
                return;
            }
            if (this.secondNumber.length() == 0) {
                this.secondNumber = this.secondNumber+"0.";
                this.insertCharInInputLabel("0.");
                return;
            }
        this.secondNumber = this.secondNumber+".";
        this.insertCharInInputLabel(".");
    }//GEN-LAST:event_JDotbtnActionPerformed

    private void JClearAllbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JClearAllbtnActionPerformed
        this.result = "";
        this.operator = "";
        this.firstNumber = "";
        this.secondNumber = "";
        this.JInputLBL.setText("");
        this.JOperationLBL.setText("");
    }//GEN-LAST:event_JClearAllbtnActionPerformed

    private void JClearbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JClearbtnActionPerformed
        if (this.JInputLBL.getText().length() == 0){
            return;
        }
        if (this.result.length() > 0) {
            this.JClearAllbtnActionPerformed(evt);
            return;
        }
        if (this.operator == null || this.operator.isEmpty()) {
            this.firstNumber = this.firstNumber.substring(0, this.firstNumber.length()-1);
            this.clear();
            return;
        }
        this.secondNumber = this.secondNumber.substring(0, this.secondNumber.length()-1);
        this.clear();
    }//GEN-LAST:event_JClearbtnActionPerformed

